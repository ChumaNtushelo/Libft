#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	size_t	n2;

	i = 0;
	n2 = 0;
	while (s1[i] != '\0')
	{
		i++;
	}
	while (s2[n2] != '\0' && n2 < n)
	{
		s1[i] = s2[n2];
		i++;
		n2++;
	}
	s1[i] = '\0';
	return (s1);
}
