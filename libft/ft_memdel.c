#include "libft.h"

void	ft_memdel(void **ap)
{
	char **ap2;

	ap2 = (char **)ap;
	if (ap == 0)
		return ;
	if (ap != NULL)
	{
		free(*ap);
	}
	*ap = NULL;
}
